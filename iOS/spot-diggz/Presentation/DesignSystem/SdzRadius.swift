import SwiftUI

/// Design system corner radius scale for SpotDiggz.
enum SdzRadius {
    /// 4pt - Small icon backgrounds
    static let xs: CGFloat = 4

    /// 8pt - Buttons, small cards, input fields
    static let sm: CGFloat = 8

    /// 12pt - Standard cards, search bars
    static let md: CGFloat = 12

    /// 16pt - Large cards, mini cards
    static let lg: CGFloat = 16

    /// 24pt - Settings cards, large panels
    static let xl: CGFloat = 24
}
